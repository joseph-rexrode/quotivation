package com.josephrexrode.quotivationproject.models;

public class ApiQuote {

	private String q;
	
	private String a;
	
	private String h;
	
	private String c;
	
	public ApiQuote() {}

	public String getQ() {
		return q;
	}

	public void setQ(String q) {
		this.q = q;
	}

	public String getA() {
		return a;
	}

	public void setA(String a) {
		this.a = a;
	}

	public String getH() {
		return h;
	}

	public void setH(String h) {
		this.h = h;
	}

	public String getC() {
		return c;
	}

	public void setC(String c) {
		this.c = c;
	}
}
